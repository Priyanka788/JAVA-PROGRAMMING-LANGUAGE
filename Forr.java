public class Forr
{
  public static void main(String args[])
  {
	 int c=5; 
  for(int i=2;i<=c;i=i+1)
  {
     System.out.println(i);
  }
  for(int j=1;j<=9;j=j+2)
  {
	  for(int r=c;r<=c;r++)
	   { 
	        System.out.println(+j+"<="+r);
	   }
      c=c+1;
      	  
  }
  
}
}   